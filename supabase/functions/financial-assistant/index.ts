import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    const systemPrompt = `You are a helpful AI financial assistant for a modern banking app. You help users with:

1. **Budgeting & Spending**: Analyze spending patterns, suggest budgets, identify areas to save money
2. **Savings Goals**: Help set realistic savings targets, suggest automatic savings amounts
3. **Bill Management**: Remind about upcoming bills, suggest autopay setups, find potential savings
4. **Credit Score**: Explain credit score factors, provide tips to improve scores
5. **Fraud Detection**: Alert about suspicious transactions, explain security best practices
6. **Financial Education**: Answer questions about banking, investing basics, financial literacy
7. **Transaction Queries**: Help users find and understand their transactions

Guidelines:
- Be friendly, professional, and encouraging
- Give specific, actionable advice when possible
- Use emojis sparingly to make responses feel modern and approachable
- Keep responses concise but helpful
- If you don't know something specific about the user's account, suggest they check their app
- Always prioritize security - never ask for sensitive info like passwords or full card numbers
- For complex investment advice, recommend speaking with a licensed financial advisor

Current context: The user has a spending account with typical monthly expenses in groceries, dining, subscriptions, and utilities.`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again in a moment." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "Service temporarily unavailable. Please try again later." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      return new Response(JSON.stringify({ error: "AI service error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("Financial assistant error:", error);
    return new Response(JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
